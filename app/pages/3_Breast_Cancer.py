# app/pages/3_🎗️_Breast_Cancer.py
# Breast Cancer prediction page.
#
# NOTE: This dataset has 30 features. We use sliders with
# medically realistic default/min/max ranges instead of plain
# number inputs, since typing 30 raw values would be impractical
# for a demo. This is a common real-world UX decision when a
# model has many features.
# ─────────────────────────────────────────────────────────────

import streamlit as st
import sys
import os

sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from utils import load_model_and_scaler, make_prediction, get_risk_level

st.set_page_config(page_title="Breast Cancer Prediction", page_icon="🎗️")

st.title("🎗️ Breast Cancer Prediction")
st.markdown("Enter cell nucleus measurements below to predict malignancy risk.")
st.markdown("---")

model, scaler = load_model_and_scaler("cancer")

# Feature names IN THE EXACT ORDER the model was trained on
FEATURE_NAMES = [
    'mean radius', 'mean texture', 'mean perimeter', 'mean area', 'mean smoothness',
    'mean compactness', 'mean concavity', 'mean concave points', 'mean symmetry', 'mean fractal dimension',
    'radius error', 'texture error', 'perimeter error', 'area error', 'smoothness error',
    'compactness error', 'concavity error', 'concave points error', 'symmetry error', 'fractal dimension error',
    'worst radius', 'worst texture', 'worst perimeter', 'worst area', 'worst smoothness',
    'worst compactness', 'worst concavity', 'worst concave points', 'worst symmetry', 'worst fractal dimension'
]

# Reasonable default values (approx. dataset means) — lets user
# adjust only the few features they want to test, rest stay realistic
DEFAULT_VALUES = [
    14.1, 19.3, 92.0, 655.0, 0.096, 0.104, 0.089, 0.049, 0.181, 0.063,
    0.405, 1.22, 2.87, 40.3, 0.007, 0.025, 0.032, 0.012, 0.020, 0.0038,
    16.3, 25.7, 107.3, 880.6, 0.132, 0.254, 0.272, 0.115, 0.290, 0.084
]

st.info("💡 Tip: Default values represent typical measurements. Adjust the sliders for the key features below to test different scenarios.")

with st.form("cancer_form"):
    st.subheader("Key Cell Measurements")

    col1, col2 = st.columns(2)

    # We expose only the TOP most important features as sliders
    # (found from our feature importance analysis in Milestone 6)
    # The rest use realistic defaults to keep the form usable
    with col1:
        mean_radius = st.slider("Mean Radius", 6.0, 30.0, DEFAULT_VALUES[0])
        mean_texture = st.slider("Mean Texture", 9.0, 40.0, DEFAULT_VALUES[1])
        mean_perimeter = st.slider("Mean Perimeter", 40.0, 190.0, DEFAULT_VALUES[2])
        worst_radius = st.slider("Worst Radius", 7.0, 37.0, DEFAULT_VALUES[20])
        worst_perimeter = st.slider("Worst Perimeter", 50.0, 252.0, DEFAULT_VALUES[22])

    with col2:
        mean_concave_points = st.slider("Mean Concave Points", 0.0, 0.2, DEFAULT_VALUES[7])
        worst_concave_points = st.slider("Worst Concave Points", 0.0, 0.3, DEFAULT_VALUES[27])
        worst_area = st.slider("Worst Area", 180.0, 4250.0, DEFAULT_VALUES[23])
        mean_area = st.slider("Mean Area", 140.0, 2500.0, DEFAULT_VALUES[3])
        worst_concavity = st.slider("Worst Concavity", 0.0, 1.3, DEFAULT_VALUES[26])

    submitted = st.form_submit_button("🔍 Predict Breast Cancer Risk", use_container_width=True)

if submitted:
    # Start with default values, then override with user's slider inputs
    input_values = DEFAULT_VALUES.copy()
    input_values[0] = mean_radius
    input_values[1] = mean_texture
    input_values[2] = mean_perimeter
    input_values[3] = mean_area
    input_values[7] = mean_concave_points
    input_values[20] = worst_radius
    input_values[22] = worst_perimeter
    input_values[23] = worst_area
    input_values[26] = worst_concavity
    input_values[27] = worst_concave_points

    prediction, confidence = make_prediction(model, scaler, input_values)
    risk_label = get_risk_level(confidence)

    st.markdown("---")
    st.subheader("Prediction Result")

    if prediction == 1:
        st.error(f"⚠️ **Malignant (Cancerous) — High Risk Detected**")
    else:
        st.success(f"✅ **Benign (Non-Cancerous) — Low Risk**")

    col1, col2 = st.columns(2)
    with col1:
        st.metric("Confidence Score", f"{confidence*100:.1f}%")
    with col2:
        st.metric("Reliability", risk_label)

    st.progress(float(confidence))

    st.info(
        "⚠️ This prediction is generated by a Machine Learning model for "
        "educational purposes. Please consult an oncologist for an actual diagnosis."
    )
