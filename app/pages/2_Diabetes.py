import streamlit as st
import sys
import os

sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from utils import load_model_and_scaler, make_prediction, get_risk_level

st.set_page_config(page_title="Diabetes Prediction", page_icon="🩸")

st.title("🩸 Diabetes Prediction")
st.markdown("Enter the patient's health metrics below to predict diabetes risk.")
st.markdown("---")

model, scaler = load_model_and_scaler("diabetes")

with st.form("diabetes_form"):
    st.subheader("Patient Information")

    col1, col2 = st.columns(2)

    with col1:
        pregnancies = st.number_input("Number of Pregnancies", min_value=0, max_value=20, value=1)
        glucose = st.number_input("Glucose Level (mg/dL)", min_value=0, max_value=300, value=100)
        blood_pressure = st.number_input("Blood Pressure (mm Hg)", min_value=0, max_value=200, value=70)
        skin_thickness = st.number_input("Skin Thickness (mm)", min_value=0, max_value=100, value=20)

    with col2:
        insulin = st.number_input("Insulin Level (mu U/ml)", min_value=0, max_value=900, value=80)
        bmi = st.number_input("BMI", min_value=0.0, max_value=70.0, value=25.0, step=0.1)
        dpf = st.number_input("Diabetes Pedigree Function", min_value=0.0, max_value=3.0, value=0.5, step=0.01,
                               help="A score based on family history of diabetes")
        age = st.number_input("Age", min_value=1, max_value=120, value=30)

    submitted = st.form_submit_button("🔍 Predict Diabetes Risk", use_container_width=True)

if submitted:
    input_values = [
        pregnancies, glucose, blood_pressure, skin_thickness,
        insulin, bmi, dpf, age
    ]

    prediction, confidence = make_prediction(model, scaler, input_values)
    risk_label = get_risk_level(confidence)

    st.markdown("---")
    st.subheader("Prediction Result")

    if prediction == 1:
        st.error(f"⚠️ **High Risk of Diabetes Detected**")
    else:
        st.success(f"✅ **Low Risk of Diabetes**")

    col1, col2 = st.columns(2)
    with col1:
        st.metric("Confidence Score", f"{confidence*100:.1f}%")
    with col2:
        st.metric("Reliability", risk_label)

    st.progress(float(confidence))

    st.info(
        "⚠️ This prediction is generated by a Machine Learning model for "
        "educational purposes. Please consult an endocrinologist for an actual diagnosis."
    )
