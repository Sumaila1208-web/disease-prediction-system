# ─────────────────────────────────────────────────────────────
# app/pages/1_❤️_Heart_Disease.py
# Heart Disease prediction page — user enters clinical values
# and gets an instant prediction.
# ─────────────────────────────────────────────────────────────

import streamlit as st
import sys
import os

sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from utils import load_model_and_scaler, make_prediction, get_risk_level

st.set_page_config(page_title="Heart Disease Prediction", page_icon="❤️")

st.title("❤️ Heart Disease Prediction")
st.markdown("Enter the patient's clinical details below to predict heart disease risk.")
st.markdown("---")

# Load model and scaler once
model, scaler = load_model_and_scaler("heart")

# ── Input form ───────────────────────────────────────────────
# Using a form groups all inputs together — prediction only runs
# when the user clicks Submit, not on every single input change
with st.form("heart_form"):
    st.subheader("Patient Information")

    col1, col2 = st.columns(2)

    with col1:
        age = st.number_input("Age", min_value=1, max_value=120, value=50)
        sex = st.selectbox("Sex", options=[("Male", 1), ("Female", 0)], format_func=lambda x: x[0])
        cp = st.selectbox(
            "Chest Pain Type",
            options=[
                ("Typical Angina", 0), ("Atypical Angina", 1),
                ("Non-anginal Pain", 2), ("Asymptomatic", 3)
            ],
            format_func=lambda x: x[0]
        )
        trestbps = st.number_input("Resting Blood Pressure (mm Hg)", min_value=80, max_value=220, value=120)
        chol = st.number_input("Serum Cholesterol (mg/dl)", min_value=100, max_value=600, value=200)
        fbs = st.selectbox("Fasting Blood Sugar > 120 mg/dl?", options=[("No", 0), ("Yes", 1)], format_func=lambda x: x[0])
        restecg = st.selectbox(
            "Resting ECG Results",
            options=[("Normal", 0), ("ST-T Wave Abnormality", 1), ("Left Ventricular Hypertrophy", 2)],
            format_func=lambda x: x[0]
        )

    with col2:
        thalach = st.number_input("Maximum Heart Rate Achieved", min_value=60, max_value=220, value=150)
        exang = st.selectbox("Exercise Induced Angina?", options=[("No", 0), ("Yes", 1)], format_func=lambda x: x[0])
        oldpeak = st.number_input("ST Depression (Exercise vs Rest)", min_value=0.0, max_value=10.0, value=1.0, step=0.1)
        slope = st.selectbox(
            "Slope of Peak Exercise ST Segment",
            options=[("Upsloping", 0), ("Flat", 1), ("Downsloping", 2)],
            format_func=lambda x: x[0]
        )
        ca = st.selectbox("Number of Major Vessels (0-3) Colored by Fluoroscopy", options=[0, 1, 2, 3])
        thal = st.selectbox(
            "Thalassemia",
            options=[("Normal", 1), ("Fixed Defect", 2), ("Reversible Defect", 3)],
            format_func=lambda x: x[0]
        )

    submitted = st.form_submit_button("🔍 Predict Heart Disease Risk", use_container_width=True)

# ── Prediction logic ────────────────────────────────────────
if submitted:
    # Build the feature array in the EXACT same order as training data
    input_values = [
        age, sex[1], cp[1], trestbps, chol, fbs[1],
        restecg[1], thalach, exang[1], oldpeak,
        slope[1], ca, thal[1]
    ]

    prediction, confidence = make_prediction(model, scaler, input_values)
    risk_label = get_risk_level(confidence)

    st.markdown("---")
    st.subheader("Prediction Result")

    if prediction == 1:
        st.error(f"⚠️ **High Risk of Heart Disease Detected**")
    else:
        st.success(f"✅ **Low Risk of Heart Disease**")

    col1, col2 = st.columns(2)
    with col1:
        st.metric("Confidence Score", f"{confidence*100:.1f}%")
    with col2:
        st.metric("Reliability", risk_label)

    st.progress(float(confidence))

    st.info(
        "⚠️ This prediction is generated by a Machine Learning model for "
        "educational purposes. Please consult a cardiologist for an actual diagnosis."
    )
